class Solution
{
    public:
    string reverseWord(string str)
    {
        // Your code goes here
        int n = str.size();
        int l = 0 , r = n-1;
        
        while(l <r){
            swap(str[l],str[r]);
            l++;
            r--;
        }
        return str;
    }
};



